export function normalize(text: string): string {
  if (!text) return "";
  const lower = text.trim().toLowerCase();
  return SYNONYMS[lower] ?? text.trim();
}

export function normalizeList(items: string[]): string[] {
  return [...new Set(items.filter(Boolean).map(normalize))];
}

// تطبيع المدن (إزالة الهمزات للمقارنة)
export function normalizeCity(city: string): string {
  return (city ?? "")
    .trim()
    .replace(/[أإآ]/g, "ا")
    .replace(/ة$/, "ه")
    .toLowerCase();
}
